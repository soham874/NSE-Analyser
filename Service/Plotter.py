def plot_data(inputData = data):

    inputData = data

    x_axis = [obj['Date'] for obj in inputData]
    close = [obj['Close'] for obj in inputData]
    reg_low = [obj['Regression Lower Band'] for obj in inputData]
    reg_med = [obj['Regression Median'] for obj in inputData]
    reg_high = [obj['Regression Upper Band'] for obj in inputData]
    close_ema = [obj['CloseEMA'] for obj in inputData]

    # Create a plotly figure
    fig = go.Figure()

    # Add traces for each graph
    fig.add_trace(go.Scatter(x=x_axis, y=close, mode='lines', name='sin(x)'))
    fig.add_trace(go.Scatter(x=x_axis, y=reg_low, mode='lines', name='cos(x)'))
    fig.add_trace(go.Scatter(x=x_axis, y=reg_med, mode='lines', name='tan(x)'))
    fig.add_trace(go.Scatter(x=x_axis, y=reg_high, mode='lines', name='sin(x)'))
    fig.add_trace(go.Scatter(x=x_axis, y=close_ema, mode='lines', name='cos(x)'))

    # Add hover text
    fig.update_layout(hovermode='x')
    fig.update_traces(
        hoverinfo='text',
        text=[f'x: {xi}<br>sin(x): {y1i}<br>cos(x): {y2i}<br>tan(x): {y3i}' 
            for xi, y1i, y2i, y3i, y4i, y5i in zip(x_axis,close,reg_low,reg_med,reg_high,close_ema)]
    )

    # Show the plot
    return fig